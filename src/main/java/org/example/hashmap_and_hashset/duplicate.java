package org.example.hashmap_and_hashset;

import java.util.HashSet;

public class duplicate {
    public boolean duplicate(int[] arr) {
        HashSet<Integer> set = new HashSet<>();
        for (int i = 0; i < arr.length; i++) {
            if (set.contains(arr[i])) {
                return true;
            } else {
                set.add(arr[i]);
            }
        }
        return false;
    }
}
